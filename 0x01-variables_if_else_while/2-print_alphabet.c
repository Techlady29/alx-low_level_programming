#include <stdio.h>

/**
 * main - assign a random number to the variable n each time it is executed
 * then print the last digit of the number stored in the n variable
 * Return: Always 0
 */

int main(void)
{
int ch;

for (ch = 97; ch <= 122; ch++)
{
putchar(ch);
}
for (ch = 65; ch <= 90; ch++)

{
putchar(ch);
}

return (0);
}
